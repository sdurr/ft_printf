#include "libft.h"
#include "libftprintf.h"
#include <stdarg.h>

int		ft_test_type(char *s)
{
	int i;

	i = -1;
	while (s[++i])
	{
		while (s[i] != '%' && s[i])
		{
			i++;

		}
		if (s[i] == '#' || s[i] == '+' || s[i] == 'l')
			i++;
		while (s[i] >= '0' && s[i] <= '9')
			i++;
		ft_putchar (s[i]);
		if ((s[i] == '%' && s[i + 1] == 'd') || (s[i] == '%' && s[i + 1] == 'i') || (s[i] == '%' && s[i + 1] == 'u') || (s[i] == '%' && s[i + 1] == 'p') || (s[i] == '%' && s[i + 1] == 's') || (s[i] == '%' && s[i + 1] == 'S') || (s[i] == '%' && s[i + 1] == '%') || (s[i] == '%' && s[i + 1] == 'x') || (s[i] == '%' && s[i + 1] == 'c') || (s[i] == '%' && s[i + 1] == 'C') || (s[i] == '%' && s[i + 1] == 'b') || (s[i] == '%' && s[i + 1] == 'o') || (s[i] == '%' && s[i + 1] == 'D') || (s[i] == '%' && s[i + 1] == 'X') || (s[i] == '%' && s[i + 1] == 'O'))
			i++;
		else
			return (-1);
	}
	return (1);
}
