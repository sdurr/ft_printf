#include <stdlib.h>

size_t		ft_longlen(long l)
{
	size_t	ret;

	ret = 1;
	if (l < -9223372036854775807)
		return (20);
	if (l < 0)
	{
		ret++;
		l *= -1;
	}
	while ((l /= 10) > 0)
		ret++;
	return (ret);
}
